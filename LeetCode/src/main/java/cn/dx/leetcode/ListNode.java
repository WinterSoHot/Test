package cn.dx.leetcode;

/**
 *
 * Definition for singly-linked list.
 * @author gudongxian
 * @version 0.1
 * @date 2020/8/18
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
